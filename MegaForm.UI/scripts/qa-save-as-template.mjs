// Visual QA for Task B: open Builder, click Save-as-Template, fill dialog,
// submit, confirm success + verify template lands in the gallery API.

import { chromium } from 'playwright-core';

const BASE = 'http://localhost:5050';
const USER = 'host';
const PASS = 'abc@ABC1024';
const CHROME = 'C:\\Program Files\\Google\\Chrome\\Application\\chrome.exe';

async function login(page) {
  await page.goto(`${BASE}/login`, { waitUntil: 'networkidle', timeout: 30000 });
  await page.waitForSelector('#username', { timeout: 10000 });
  await page.fill('#username', USER);
  await page.fill('#password', PASS);
  await page.locator('button.btn-primary:has-text("Login")').first().click();
  await page.waitForFunction(() => !location.pathname.toLowerCase().includes('/login'), null, { timeout: 20000 }).catch(()=>{});
  await page.waitForTimeout(1500);
}

(async () => {
  const browser = await chromium.launch({ executablePath: CHROME, headless: true });
  const ctx = await browser.newContext({ viewport: { width: 1440, height: 900 } });
  const page = await ctx.newPage();
  const errors = [];
  page.on('console', m => { if (m.type() === 'error') errors.push(`[console.error] ${m.text().slice(0,200)}`); });
  page.on('pageerror', e => errors.push(`[pageerror] ${String(e).slice(0,200)}`));

  await login(page);
  console.log('[login] OK');

  // Navigate via the Dashboard sidebar to Form Builder so we land on a real form.
  // Module 190 (Site 3 "business") is bound to Leave Request Starter (form 10).
  await page.goto(`${BASE}/business/*/190/Builder?formId=10`, { waitUntil: 'networkidle', timeout: 45000 });
  // Wait for the builder topbar + Save-as-Template button
  await page.waitForSelector('#mf-btn-save-as-template', { timeout: 30000 });
  await page.waitForTimeout(1500);
  console.log('[builder loaded] Save-as-Template button visible');
  await page.screenshot({ path: 'qa-out/sat-01-builder-with-button.png', fullPage: false });

  // Click Save-as-Template. Multiple overlays exist because the Oqtane
  // fullscreen takeover clones the builder root; .first() picks the live one.
  await page.click('#mf-btn-save-as-template');
  await page.waitForFunction(() => !!document.querySelector('.mf-sat-overlay'), null, { timeout: 5000 });
  await page.waitForTimeout(500);
  console.log('[dialog opened]');

  // Inspect dialog regions
  const dlg = await page.evaluate(() => {
    const ov = document.querySelector('.mf-sat-overlay');
    if (!ov) return null;
    return {
      title:       ov.querySelector('.mf-sat-title-bar')?.textContent?.trim(),
      hasName:     !!ov.querySelector('input.mf-sat-input'),
      hasDesc:     !!ov.querySelector('textarea.mf-sat-textarea'),
      defaultName: (ov.querySelectorAll('input.mf-sat-input')[0])?.value,
      fields:      ov.querySelectorAll('.mf-sat-field').length,
      saveBtn:     !!ov.querySelector('.mf-sat-btn--primary'),
    };
  });
  console.log('[dialog]', JSON.stringify(dlg));
  await page.screenshot({ path: 'qa-out/sat-02-dialog-opened.png', fullPage: false });

  // Fill custom name + description
  const ts = Date.now();
  const newTitle = 'QA Leave Request Template ' + ts;
  // Oqtane fullscreen takeover puts overlay inside a hoisted clone — Playwright's
  // visibility heuristic fails. Set values via JS and dispatch input events
  // so the dialog's own handlers pick them up.
  await page.evaluate(({ title, desc }) => {
    const dialogs = Array.from(document.querySelectorAll('.mf-sat-overlay'));
    const ov = dialogs[dialogs.length - 1];  // newest (just created)
    if (!ov) return;
    const inp = ov.querySelector('input.mf-sat-input');
    const ta  = ov.querySelector('textarea.mf-sat-textarea');
    if (inp) { inp.value = title; inp.dispatchEvent(new Event('input', { bubbles: true })); }
    if (ta)  { ta.value  = desc;  ta.dispatchEvent(new Event('input',  { bubbles: true })); }
  }, { title: newTitle, desc: 'Auto-generated by qa-save-as-template at ' + new Date().toISOString() });

  // Capture all template gallery entries BEFORE save (via API)
  const beforeList = await page.evaluate(async () => {
    const r = await fetch('/api/MegaForm/BuilderTemplates/List', { credentials: 'same-origin' });
    if (!r.ok) return { ok: false, status: r.status };
    const data = await r.json();
    return { ok: true, count: Array.isArray(data) ? data.length : 0 };
  });
  console.log('[gallery before]', JSON.stringify(beforeList));

  // Click Save to Gallery (scope to visible dialog)
  await visibleDlg.locator('.mf-sat-btn--primary').first().click();
  // Wait for success toast or dialog dismiss
  await page.waitForTimeout(2500);
  await page.screenshot({ path: 'qa-out/sat-03-after-save.png', fullPage: false });

  const result = await page.evaluate((title) => {
    const ov = document.querySelector('.mf-sat-overlay');
    const toast = document.querySelector('.mf-sat-toast');
    return {
      dialogStillOpen: !!ov,
      toastText: toast?.textContent || '',
      toastKind:  toast?.className || '',
    };
  }, newTitle);
  console.log('[immediate result]', JSON.stringify(result));

  // Verify gallery NOW contains the new template
  const afterList = await page.evaluate(async (title) => {
    const r = await fetch('/api/MegaForm/BuilderTemplates/List', { credentials: 'same-origin' });
    if (!r.ok) return { ok: false, status: r.status };
    const data = await r.json();
    const arr = Array.isArray(data) ? data : [];
    const found = arr.filter(t => (t.title || t.Title) === title);
    return {
      ok: true,
      count: arr.length,
      foundCount: found.length,
      matched: found.slice(0, 1).map(t => ({
        title: t.title || t.Title,
        category: t.category || t.Category,
        icon: t.icon || t.Icon,
        fieldCount: Array.isArray(t.fields || t.Fields) ? (t.fields || t.Fields).length : 0,
        fileName: t.fileName || t.FileName,
      })),
    };
  }, newTitle);
  console.log('[gallery after]', JSON.stringify(afterList, null, 2));

  console.log('\n=== SUMMARY ===');
  console.log('Dialog opened:        ', !!dlg);
  console.log('4 form fields rendered:', dlg?.fields === 4);
  console.log('Dialog closed on save:', !result.dialogStillOpen);
  console.log('Toast success:        ', /success/i.test(result.toastKind));
  console.log('Gallery count grew:   ', (afterList.count > beforeList.count));
  console.log('Template found by name:', afterList.foundCount > 0);
  console.log('Saved record details: ', JSON.stringify(afterList.matched[0] || {}));

  if (errors.length) console.log('\n--- ERRORS ---\n' + errors.join('\n'));
  else console.log('(0 console errors)');

  await browser.close();
})();
