  function parseResponse(r: Response): Promise<any> {
    return r.text().then(function (txt) {
      var data: any = null;
      try { data = txt ? JSON.parse(txt) : null; } catch (_e) { data = txt; }
      if (!r.ok) {
        var msg = (data && (data.error || data.message)) || txt || ('HTTP ' + r.status);
        throw new Error(msg);
      }
      return data;
    });
  }
