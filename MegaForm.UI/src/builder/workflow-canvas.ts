    function FieldInsertButton(props: any): any {
      var useState = R.useState;
      var openState = useState(false);
      var open = openState[0], setOpen = openState[1];
      var targetId = props.targetId;
      function insertText(text: string): void {
        var el = document.getElementById(targetId) as HTMLInputElement | HTMLTextAreaElement | null;
        if (!el) return;
        var start = typeof el.selectionStart === 'number' ? el.selectionStart : el.value.length;
        var end = typeof el.selectionEnd === 'number' ? el.selectionEnd : el.value.length;
        var before = el.value.substring(0, start);
        var after = el.value.substring(end);
        el.value = before + text + after;
        if (el.dispatchEvent) el.dispatchEvent(new Event('input', { bubbles: true }));
        setOpen(false);
        try {
          el.focus();
          var pos = before.length + text.length;
          el.setSelectionRange(pos, pos);
        } catch (_e) { }
      }
      return h('div', { className: 'mf-rf-picker' },
        h('button', { type: 'button', className: 'mf-rf-picker__btn', onClick: function () { setOpen(!open); } }, '📋'),
        open && h('div', { className: 'mf-rf-picker__menu' },
          (schema.fields || []).map(function (f) {
            var token = '{{field.' + f.key + '}}';
            return h('button', { type: 'button', key: token, className: 'mf-rf-picker__item', onClick: function () { insertText(token); } }, token);
          })
        )
      );
    }

    function ConditionGroupEditor(props: any): any {
      var groups = props.groups || [];
      var setGroups = props.setGroups;
      function patchGroup(idx: number, next: ConditionGroup): void {
        setGroups(groups.map(function (g: ConditionGroup, i: number) { return i === idx ? next : g; }));
      }
      function removeGroup(idx: number): void {
        var next = groups.filter(function (_g: ConditionGroup, i: number) { return i !== idx; });
        if (!next.length) next = [{ logic: 'and', rules: [{ fieldKey: '', operator: 'equals', value: '', valueType: 'literal' }] }];
        setGroups(next);
      }
      function addGroup(): void {
        setGroups(groups.concat([{ logic: 'and', rules: [{ fieldKey: '', operator: 'equals', value: '', valueType: 'literal' }] }]));
      }
      return h('div', null,
        groups.map(function (group: ConditionGroup, gi: number) {
          return h('div', { key: 'g' + gi, className: 'mf-rf-cond-group' },
            h('div', { className: 'mf-rf-cond-group__head' },
              h('strong', null, 'Group ' + (gi + 1)),
              h('div', { className: 'mf-rf-inline-actions' },
                h('button', { type: 'button', className: 'mf-rf-cfg-btn', onClick: function () {
                  var next = Object.assign({}, group, { rules: group.rules.concat([{ fieldKey: '', operator: 'equals', value: '', valueType: 'literal' }]) });
                  patchGroup(gi, next);
                } }, '+ Rule'),
                h('button', { type: 'button', className: 'mf-rf-cfg-btn mf-rf-cfg-btn--danger', onClick: function () { removeGroup(gi); } }, '✕')
              )
            ),
            cfgField('Logic', h('select', {
              className: 'mf-rf-cfg-input', value: group.logic,
              onChange: function (e: any) { patchGroup(gi, Object.assign({}, group, { logic: e.target.value })); }
            }, h('option', { value: 'and' }, 'AND'), h('option', { value: 'or' }, 'OR'))),
            group.rules.map(function (rule: ConditionRule, ri: number) {
              var field = getFieldByKey(rule.fieldKey);
              var operators = getOperatorsForFieldType(field ? field.type : 'Text');
              var needsNoValue = rule.operator === 'isEmpty' || rule.operator === 'isNotEmpty';
              var valueInput: any;
              if (rule.valueType === 'field') {
                valueInput = h('select', {
                  className: 'mf-rf-cfg-input', value: rule.value,
                  onChange: function (e: any) { updateRule(gi, ri, { value: e.target.value }); }
                }, h('option', { value: '' }, 'Select field...'), (schema.fields || []).map(function (f) { return h('option', { key: f.key, value: f.key }, f.label + ' (' + f.key + ')'); }));
              } else if (field && (field.type === 'Select' || field.type === 'Radio') && rule.valueType === 'literal') {
                valueInput = h('select', {
                  className: 'mf-rf-cfg-input', value: rule.value,
                  onChange: function (e: any) { updateRule(gi, ri, { value: e.target.value }); }
                }, h('option', { value: '' }, 'Select option...'), (field.options || []).map(function (o) { return h('option', { key: o.value, value: o.value }, o.label + ' (' + o.value + ')'); }));
              } else {
                valueInput = h('input', {
                  className: 'mf-rf-cfg-input', value: rule.value, placeholder: rule.valueType === 'variable' ? 'variable name' : 'compare value',
                  onChange: function (e: any) { updateRule(gi, ri, { value: e.target.value }); }
                });
              }
              var updateRule = function (gidx: number, ridx: number, patch: any): void {
                var nextRules = group.rules.map(function (r: ConditionRule, i: number) { return i === ridx ? Object.assign({}, r, patch) : r; });
                patchGroup(gidx, Object.assign({}, group, { rules: nextRules }));
              };
              return h('div', { key: 'r' + ri, className: 'mf-rf-cond-rule' },
                cfgField('Field', h('select', {
                  className: 'mf-rf-cfg-input', value: rule.fieldKey,
                  onChange: function (e: any) {
                    var newField = getFieldByKey(e.target.value);
                    var nextOperator = getOperatorsForFieldType(newField ? newField.type : 'Text')[0];
                    updateRule(gi, ri, { fieldKey: e.target.value, operator: nextOperator, value: '' });
                  }
                }, h('option', { value: '' }, 'Select field...'), (schema.fields || []).map(function (f) { return h('option', { key: f.key, value: f.key }, f.label + ' (' + String(f.type || '').toLowerCase() + (f.required ? ', required' : '') + ')'); }))),
                cfgField('Operator', h('select', {
                  className: 'mf-rf-cfg-input', value: rule.operator,
                  onChange: function (e: any) { updateRule(gi, ri, { operator: e.target.value }); }
                }, operators.map(function (op) { return h('option', { key: op, value: op }, op); }))),
                cfgField('Compare To', h('div', null,
                  h('label', { className: 'mf-rf-radio' }, h('input', { type: 'radio', name: 'vt-' + gi + '-' + ri, checked: rule.valueType === 'literal', onChange: function () { updateRule(gi, ri, { valueType: 'literal', value: '' }); } }), h('span', null, 'Value')),
                  h('label', { className: 'mf-rf-radio' }, h('input', { type: 'radio', name: 'vt-' + gi + '-' + ri, checked: rule.valueType === 'field', onChange: function () { updateRule(gi, ri, { valueType: 'field', value: '' }); } }), h('span', null, 'Field')),
                  h('label', { className: 'mf-rf-radio' }, h('input', { type: 'radio', name: 'vt-' + gi + '-' + ri, checked: rule.valueType === 'variable', onChange: function () { updateRule(gi, ri, { valueType: 'variable', value: '' }); } }), h('span', null, 'Variable'))
                )),
                !needsNoValue && cfgField('Value', valueInput),
                h('button', { type: 'button', className: 'mf-rf-cfg-btn mf-rf-cfg-btn--danger', onClick: function () {
                  var nextRules2 = group.rules.filter(function (_r: ConditionRule, i: number) { return i !== ri; });
                  if (!nextRules2.length) nextRules2 = [{ fieldKey: '', operator: 'equals', value: '', valueType: 'literal' }];
                  patchGroup(gi, Object.assign({}, group, { rules: nextRules2 }));
                } }, 'Remove rule')
              );
            })
          );
        }),
        h('button', { type: 'button', className: 'mf-rf-cfg-btn', onClick: addGroup }, '+ Add Group')
      );
    }

